    $(document).on('click', '.ajax-rest-post', function(e) {
        e.preventDefault();

        var $btn = $(this);

        var $text = $btn.text();

        var parent = false;

        var form = $(this).parents('form');

        $.ajax({
            url: form.attr('action'),
            async: false,
            type: 'POST',
            dataType: 'json',
            xhrFields: {
                withCredentials: true
            },
            crossDomain: true,
            data: form.serializeArray(),
            beforeSend: function() {
                $btn.text('Enviando...');

                $('label.validation').remove();

                $('.send-alert').remove();

                $('.has-error').each(function() {
                    $(this).removeClass('has-error');
                });
            },
            success: function(data) {
                if (data.type == 'success') {
                    form.prepend('<div class="alert send-alert alert-success">' + data.message + '</div>');

                    form.find('input[type="text"],input[type="password"],textarea').val('');

                    if (data.redirect) {
                        window.location.href = data.redirect;
                    }

                    setTimeout(function() {
                        $('.send-alert').remove();
                    }, 10000);
                }

                if (data.type == 'error') {
                    $(form).prepend('<div class="alert send-alert alert-danger">' + data.message + '</div>');

                    // Exibe o aviso de erro em cada campo
                    $.each(data.validation, function(key, value)
                    {
                        // Capturar elemento pai
                        parent = $(form).find('[name="' + key + '"],[name="' + key + '[]"]').parents('div:first');

                        // Verifica qual o tipo do elemento pai
                        if(parent.hasClass('input-group') || parent.hasClass('radio') || parent.hasClass('checkbox'))
                        {
                            parent = $(form).find('[name="' + key + '"],[name="' + key + '[]"]').parents('div:first').parent();
                        }
                        else
                        {
                            parent = $(form).find('[name="' + key + '"],[name="' + key + '[]"]').parents('div:first');
                        }

                        // Insere a mensagem de erro
                        $(parent).append('<label class="validation text-danger">' + value + '</label>');
                        $(parent).children('.validation').removeClass('hidden');

                        // Insere classe de erro no form-group
                        $(parent).addClass('has-error');
                    });
                }
            },
            complete: function() {
                setTimeout(function() {
                    $btn.text($text);
                }, 300);
            }
        });
    });

